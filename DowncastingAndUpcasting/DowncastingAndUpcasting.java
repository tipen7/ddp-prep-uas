package DowncastingAndUpcasting;

/**
 * CONCEPTUAL NOTES: UPCASTING AND DOWNCASTING
 * * 1. Upcasting: Casting a subclass reference to a superclass type (e.g., `SuperClass obj = new SubClass();`).
 * This happens automatically/implicitly because a subclass object "is-a" instance of its parent.
 * 2. Downcasting: Casting a superclass reference back to a subclass type (e.g., `SubClass sub = (SubClass) obj;`).
 * This must be done explicitly.
 * 3. Runtime Type Check: If the actual underlying object instance in memory does not match
 * the target downcast class type (or its descendants), a `ClassCastException` is thrown.
 * 4. Polymorphic Method Resolution: Variables resolve fields based on their static declared reference type,
 * but resolve overridden non-static methods based on the dynamic underlying object instance type at runtime.
 */

